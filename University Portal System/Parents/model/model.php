function addRegistrationInfo($data){
	$conn = db_conn();
    $selectQuery = "INSERT into parents_info (A_NAME, EMAIL,USERNAME,PASSWORD,PHONE)
VALUES (:name, :email, :username, :password, :phone)";
    try{
        $stmt = $conn->prepare($selectQuery);
        $stmt->execute([
        	':name' => $data['name'],
        	':email' => $data['email'],
					':username' => $data['username'],
					':password' => $data['password'],
					':phone' => $data['phone']

          ]);
    }catch(PDOException $e){
        echo $e->getMessage();
    }

    $conn = null;
    return true;
}








